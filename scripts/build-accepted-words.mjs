#!/usr/bin/env node

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const ALBANIAN_DIGRAPHS = new Set([
  "dh",
  "gj",
  "ll",
  "nj",
  "rr",
  "sh",
  "th",
  "xh",
  "zh",
]);

const ALBANIAN_SINGLE_LETTERS = new Set([
  "a",
  "b",
  "c",
  "ç",
  "d",
  "e",
  "ë",
  "f",
  "g",
  "h",
  "i",
  "j",
  "k",
  "l",
  "m",
  "n",
  "o",
  "p",
  "q",
  "r",
  "s",
  "t",
  "u",
  "v",
  "x",
  "y",
  "z",
]);

const sourcePath = resolve(process.argv[2] ?? "third_party/sq_AL/sq_AL.dic");
const outputPath = resolve(process.argv[3] ?? "src/accepted-words.js");

function dictionaryRoot(line) {
  const entry = line.trim().split(/\s+/u, 1)[0];
  return entry.split("/", 1)[0].normalize("NFC");
}

function tokenizeAlbanian(word) {
  const tokens = [];

  for (let index = 0; index < word.length; ) {
    const pair = word.slice(index, index + 2);

    if (ALBANIAN_DIGRAPHS.has(pair)) {
      tokens.push(pair);
      index += 2;
      continue;
    }

    const letter = word[index];
    if (!ALBANIAN_SINGLE_LETTERS.has(letter)) {
      return null;
    }

    tokens.push(letter);
    index += 1;
  }

  return tokens;
}

function isFiveLetterLowercaseAlbanian(root) {
  if (!root || root !== root.toLocaleLowerCase("sq-AL")) {
    return false;
  }

  const tokens = tokenizeAlbanian(root);
  return tokens !== null && tokens.length === 5;
}

const dictionary = await readFile(sourcePath, "utf8");
const lines = dictionary.split(/\r?\n/u);
const declaredEntryCount = Number.parseInt(lines[0], 10);

if (!Number.isInteger(declaredEntryCount)) {
  throw new Error(`Invalid Hunspell header in ${sourcePath}`);
}

const acceptedWords = [
  ...new Set(
    lines
      .slice(1)
      .map(dictionaryRoot)
      .filter(isFiveLetterLowercaseAlbanian),
  ),
].sort((left, right) => left.localeCompare(right, "sq-AL"));

const generatedSource = `// Generated and modified by FJALË contributors on 2026-07-16.
// Source: Albanian MySpell/Hunspell dictionary v1.6.4 by Luan Kelmendi,
// distributed by LibreOffice under GPL-2.0-or-later.
// See THIRD_PARTY_NOTICES.md. Do not edit this file by hand.

export const ACCEPTED_WORDS = Object.freeze(${JSON.stringify(acceptedWords)});

export const ACCEPTED_WORD_SET = new Set(ACCEPTED_WORDS);
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, generatedSource, "utf8");

console.log(
  `Generated ${acceptedWords.length.toLocaleString("en-US")} accepted Albanian words from ${declaredEntryCount.toLocaleString("en-US")} Hunspell entries.`,
);
console.log(`Wrote ${outputPath}`);
