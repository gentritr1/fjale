const REQUIRED_METADATA_FIELDS = [
  "word",
  "partOfSpeech",
  "syllables",
  "clue",
  "definition",
  "example",
  "region",
];

const seenAnswers = new Set();

for (const answer of ANSWER_ENTRIES) {
  for (const field of REQUIRED_METADATA_FIELDS) {
    if (typeof answer[field] !== "string" || answer[field].trim() === "") {
      throw new Error(`Answer ${answer.word ?? "<unknown>"} is missing ${field}`);
    }
  }

  const tokens = tokenizeAlbanian(answer.word);
  if (tokens === null || tokens.length !== 5) {
    throw new Error(`${answer.word} is not exactly five Albanian letters`);
  }

  if (answer.word !== answer.word.normalize("NFC").toLocaleLowerCase("sq-AL")) {
    throw new Error(`${answer.word} must be lowercase Unicode NFC`);
  }

  if (answer.region !== "standard") {
    throw new Error(`${answer.word} must be marked as standard Albanian`);
  }

  if (seenAnswers.has(answer.word)) {
    throw new Error(`Duplicate answer: ${answer.word}`);
  }

  seenAnswers.add(answer.word);
}

export const ANSWERS = Object.freeze(ANSWER_ENTRIES.map((answer) => Object.freeze(answer)));

