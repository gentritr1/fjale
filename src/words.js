function tokenizeAlbanian(word) {
  const normalizedWord = word.normalize("NFC").toLocaleLowerCase("sq-AL");
  const tokens = [];

  for (let index = 0; index < normalizedWord.length; ) {
    const pair = normalizedWord.slice(index, index + 2);

    if (ALBANIAN_DIGRAPHS.has(pair)) {
      tokens.push(pair);
      index += 2;
      continue;
    }

    const letter = normalizedWord[index];
    if (!ALBANIAN_SINGLE_LETTERS.has(letter)) {
      return null;
    }

    tokens.push(letter);
    index += 1;
  }

  return tokens;
}

